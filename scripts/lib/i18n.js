/** 模板渲染: fmt('hi {name}', {name:'bob'}) → 'hi bob'. */
function fmt(tpl, params) {
  if (!params) return tpl;
  return tpl.replace(/\{([a-zA-Z0-9_]+)\}/g, (m, k) => (params[k] != null ? String(params[k]) : m));
}

/** 取指定语言字典; 缺失 key 兜底到英语. */
function makeT(lang) {
  const primary = DICT[lang] || DICT.en;
  const fallback = DICT.en;
  return function t(key, params) {
    const val = primary[key];
    const raw = val != null ? val : fallback[key];
    if (raw == null) return key;
    if (typeof raw === 'string') return fmt(raw, params);
    return raw;
  };
}

